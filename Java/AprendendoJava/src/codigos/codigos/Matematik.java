package codigos.codigos;

public class Matematik {
			  
	  // metodos genéricos mas utéis quem sabe xD
	  
	  public double elevarA(double x, double p){
		  return  Math.pow(x, p);
	  }
	  
	  
	  public int quad(int x){
		  return x*x;
	  }
	  
	  
	  public int triplo(int x){
		  return x*x*x;
	  }
	  
	  
	  public double areaT(double b, double h){
		  	return (b*h)/2;
	  }
}
